#include "shadow.h"
#include "resource_texture.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CShadow::CShadow()
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CShadow::~CShadow()
{
}

//=============================================================================
// 生成処理
//=============================================================================
CShadow * CShadow::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	CShadow* pShadow = NULL;
	pShadow = new CShadow;

	if (pShadow != NULL)
	{
		pShadow->Init();
		pShadow->SetPos(pos);
		pShadow->SetSize(size);
		pShadow->SetAngle(0.0f);
		pShadow->SetColor(D3DXCOLOR(0.2f, 0.2f, 0.2f, 0.3f));
		pShadow->BindTexture(CResourceTexture::GetTexture(CResourceTexture::TEXTURE_SHADOW));

	}
	return pShadow;
}

//=============================================================================
// 更新処理
//=============================================================================
void CShadow::Update(void)
{
	CScene3d::Update();
}
