//=============================================================================
// インクルードファイル
//=============================================================================
#include "button.h"
#include "keyboard.h"
#include "manager.h"

//=============================================================================
// マクロ定義
//=============================================================================
#define BUTTON_COLOR D3DXCOLOR(1.0f,1.0f,1.0f,1.0f)		// カラー
#define NOT_BUTTON_COLOR D3DXCOLOR(1.0f,1.0f,1.0f,0.5f)	// 選択されてないときのカラー
//=============================================================================
// コンストラクタ
//=============================================================================
CButton::CButton() :CScene2d(OBJTYPE_UI)
{
	m_color = BUTTON_COLOR;	// カラー
}

//=============================================================================
// デストラクタ
//=============================================================================
CButton::~CButton()
{

}

//=============================================================================
// 生成処理
//=============================================================================
CButton * CButton::Create(D3DXVECTOR3 pos)
{
	// メモリの確保
	CButton *pButton;
	pButton = new CButton;

	// 初期化
	pButton->Init();

	// 各値の代入
	pButton->SetPos(pos);
	pButton->SetPriority(OBJTYPE_UI);

	return pButton;
}

//=============================================================================
// 初期化処理
//=============================================================================
HRESULT CButton::Init(void)
{
	// 初期化処理
	CScene2d::Init();

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CButton::Uninit(void)
{
	// 終了処理
	CScene2d::Uninit();
}

//=============================================================================
// 更新処理
//=============================================================================
void CButton::Update(void)
{
	// 更新処理
	CScene2d::Update();
}

//=============================================================================
// 描画処理
//=============================================================================
void CButton::Draw(void)
{
	// 描画処理
	CScene2d::Draw();
}

//=============================================================================
// 選択中の色の処理
//=============================================================================
void CButton::SelectColor(void)
{
	SetColor(BUTTON_COLOR);
}

//=============================================================================
// 選択されてない時の色の処理
//=============================================================================
void CButton::NotSelectColor(void)
{
	SetColor(NOT_BUTTON_COLOR);
}